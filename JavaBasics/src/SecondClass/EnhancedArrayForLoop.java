package SecondClass;

public class EnhancedArrayForLoop {
	public static void main(String[] args) {
		String names[] = {"Ethan","Ivnaka","Thomas","Arun","Deepika"};
		int age[]= {20,30,40,50,60};
		for (int j:age)
		{
			System.out.println(j);
		}
		for(String i:names)
		{
			System.out.println(i);
		}
	}

}
